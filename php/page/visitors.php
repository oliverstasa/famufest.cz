  <h1>FESTIVALOVÉ AKREDITACE</h1>
  <div class="content">
  Návštěva letošního festivalu je úzce spojena s koupí akreditace, která vám umožní:<br>
  <br>
  Osobní vyzvedávání lístků na jednotlivé projekce na festivalové pokladně<br>
  <br>
  Vstup do kinosálu bez vyzvednutého lístku, v případě kdy 10 minut před projekcí dojde k uvolnění rezervovaných míst v sále<br>
  <br>
  Vstup na veškerý doprovodný program festivalu<br>
  <br>
  Zajímavé slevy v partnerských podnicích FAMUFESTU<br>
  </div>
